//: 扩展: 为既有类型添加新功能,即使无权查看其源代码(也叫:追加建模)
//: - 范围: 增加计算属性，方法（包括构造器、下标）; 嵌套类型; 协议遵从。

//: - MVC模式中的一个最佳实践：如果一个类其中的属性和方法过多（iOS开发中通常ViewController代码量很大），应该考虑把代码分离。但如果不适合分离的，写成扩展也是一种很好的方式。




