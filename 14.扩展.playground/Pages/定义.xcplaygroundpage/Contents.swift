//: 扩展: 为既有类型添加新功能,即使无权查看源代码(也叫:追加建模)
//: - 范围: 扩展计算属性，方法（包括构造器、下标）; 扩展嵌套类型; 扩展协议。

//: - 一种分离代码的“模式”：如果一个类其中的属性和方法过多（iOS开发中通常ViewController代码量很大），应当考虑把代码分离。写成扩展是一种很直接的方式。（见iOS 11开发第一季中的FansArea例子）




